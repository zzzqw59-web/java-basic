package lecture;

public class Binary {
    public static void main(String[] args) {
        int num = 10;
        int bNum = 0B1010; //10
        int oNum = 012; //10
        int hNum = 0XA;

        System.out.println(num);
        System.out.println(bNum);
        System.out.println(oNum);
        System.out.println(hNum);
    }
}
